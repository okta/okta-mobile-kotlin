package com.okta.authfoundation.util

import kotlinx.coroutines.CancellationException

// Like `runCatching`, but rethrows `CancellationException` instead of wrapping it in a
// `Result.failure`, preserving structured concurrency cancellation semantics.
internal suspend inline fun <T> runCatchingCancellable(crossinline block: suspend () -> T): Result<T> =
    try {
        Result.success(block())
    } catch (cancellationException: CancellationException) {
        throw cancellationException
    } catch (exception: Exception) {
        Result.failure(exception)
    }
